import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const envPath = path.join(root, '.env');
const outPath = path.join(root, 'src/builtin-env.ts');

let apiKey = '';

if (fs.existsSync(envPath)) {
  const content = fs.readFileSync(envPath, 'utf-8');
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const match = trimmed.match(/^PEXELS_API_KEY=(.*)$/);
    if (match) {
      apiKey = match[1].trim().replace(/^["']|["']$/g, '');
      break;
    }
  }
}

if (!apiKey) {
  console.warn('Warning: PEXELS_API_KEY not found in .env');
}

const source = `// Auto-generated by scripts/generate-builtin-env.mjs — do not edit manually
export const PEXELS_API_KEY = ${JSON.stringify(apiKey)};
`;

fs.writeFileSync(outPath, source);
console.log('Generated src/builtin-env.ts');
